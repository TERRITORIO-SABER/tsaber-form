<script setup lang="ts">
import Joi from 'joi'
import type { FormSubmitEvent } from '#ui/types'
import { cpf } from 'cpf-cnpj-validator'
import { POSITION, useToast } from 'vue-toastification'
import { CommonOptions } from 'vue-toastification/dist/types/types'

const toast = useToast()
const toastOptions: CommonOptions = {
  position: POSITION.TOP_CENTER
}

const requestTypeOptions = [
  {
    name: 'D칰vidas',
    value: 'duvida'
  },
  {
    name: 'Problema',
    value: 'problema'
  },
  {
    name: 'Outros',
    value: 'outros'
  }
]
const subjectOptions = [
  {
    name: 'Suporte T칠cnico',
    value: 'suporte_tecnico'
  }
]

const schema = Joi.object({
  email: Joi.string()
    .email({ tlds: { allow: false } })
    .required()
    .custom((value, helpers) => {
      if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(value)) {
        return helpers.message({ custom: 'Digite um Email v치lido' })
      }
      return value
    }, 'Email Validation')
    .messages({
      'any.required': `E-mail n칚o pode ficar em branco`,
      '*': 'Digite um e-mail v치lido'
    }),
  messageTitle: Joi.string().required().messages({
    '*': `T칤tulo da mensagem n칚o pode ficar em branco`
  }),
  description: Joi.string().required().messages({
    '*': `Descri칞칚o n칚o pode ficar em branco`
  }),
  requestType: Joi.string()
    .default(null)
    .valid('duvida', 'problema', 'outros')
    .required()
    .messages({
      '*': `Escolha uma op칞칚o`
    }),
  subject: Joi.string().required().messages({
    '*': `Escolha uma op칞칚o`
  }),
  fullName: Joi.string().required().messages({
    '*': `Nome completo n칚o pode ficar em branco`
  }),
  cpf: Joi.string()
    .required()
    .custom((value, helpers) => {
      if (!cpf.isValid(value)) {
        return helpers.message({ custom: 'CPF inv치lido' })
      }
      return value
    }, 'CPF Validation')
    .messages({
      '*': 'CPF inv치lido'
    }),
  phone: Joi.string()
    .required()
    .custom((value, helpers) => {
      // Check if the numericValue matches the expected format
      if (!/^\(\d{2}\) \d{5}-\d{4}$/.test(value)) {
        return helpers.message({
          custom: 'O telefone deve seguir o formato (##) #####-####'
        })
      }
      // Check if all values in the string are the same
      function allEqual(input: string) {
        return input.split('').every((char) => char === input[0])
      }
      const numericValue = value.replace(/\D/g, '')
      if (allEqual(numericValue)) {
        return helpers.message({
          custom: 'Insira um telefone v치lido'
        })
      }
      return value
    }, 'Phone Validation')
    .messages({
      '*': `Insira um telefone v치lido`
    }),
  orderNumber: Joi.string()
    .allow('', null)
    .custom((value, helpers) => {
      // Check if the value contains only numbers
      if (value && !/^\d+$/.test(value)) {
        return helpers.message({
          custom: 'O n칰mero do pedido deve conter apenas n칰meros'
        })
      }
      return value
    }, 'Order Number Validation'),
  requestAttachments: Joi.object({
    name: Joi.string(),
    file: Joi.any()
  })
})

const defaultValues = {
  email: null,
  messageTitle: null,
  description: null,
  requestType: null,
  subject: null,
  fullName: null,
  cpf: null,
  phone: null,
  orderNumber: null,
  requestAttachments: {
    name: '',
    file: null
  }
}
const state = ref({
  ...defaultValues
})

let isLoading = ref(false)
let fileInput = ref()

const selectedFileChanged = async (event: Event) => {
  const input = event.target as HTMLInputElement
  if (input?.files?.length === 0) {
    console.log('No file selected.')
    return
  }
  const file = input?.files?.[0] as File
  const localFile = await fetch(URL.createObjectURL(file))
  const blob = await localFile.blob()

  state.value.requestAttachments.file = blob as any
  state.value.requestAttachments.name = file?.name || ''
}

async function onSubmit(event: FormSubmitEvent<any>) {
  isLoading.value = true
  try {
    const { $recaptcha } = useNuxtApp()
    // @ts-ignore
    const token = await $recaptcha.execute('login')
    if (!token) throw new Error('Recaptcha failed')

    const { file, name } = state.value.requestAttachments
    if (file) {
      const formData = new FormData()
      formData.append('file', file, name)
      const res = await fetch('/api/upload', {
        method: 'POST',
        body: formData
      }).then((result) => result.json())

      if (res.token) {
        event.data.fileToken = res.token
        event.data.fileName = state.value.requestAttachments.name
      }
    }
    const response = await fetch('/api/form', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json'
      },
      body: JSON.stringify(event.data)
    })

    if (!response?.ok) {
      throw new Error('Failed to submit form')
    }
    toast.success('Solicita칞칚o enviada com sucesso! 游때', toastOptions)
  } catch (error) {
    console.error('Error submitting form:', error)
    toast.error(
      'Algo deu errado 游땬, por favor tente novamente em alguns minutos...',
      toastOptions
    )
  } finally {
    isLoading.value = false
    state.value = defaultValues
    fileInput.value = null
  }
}
</script>

<template>
  <div>
    <h1 class="text-[1.5rem] text-xs-[2rem] font-bold mb-4">
      Enviar uma solicita칞칚o
    </h1>

    <UForm
      id="request-form"
      :schema="schema"
      :state="state"
      class="space-y-4 mb-4"
      @submit="onSubmit"
    >
      <UFormGroup label="Endere칞o de e-mail *" name="email">
        <UInput v-model="state.email" placeholder="exemplo@example.com" />
      </UFormGroup>

      <UFormGroup label="T칤tulo da mensagem *" name="messageTitle">
        <UInput v-model="state.messageTitle" />
      </UFormGroup>

      <UFormGroup label="Descri칞칚o *" name="description">
        <UTextarea v-model="state.description" />
      </UFormGroup>

      <UFormGroup label="Tipo de solicita칞칚o *" name="requestType">
        <USelect
          v-model="state.requestType"
          :options="requestTypeOptions"
          option-attribute="name"
          placeholder="-"
        />
      </UFormGroup>

      <UFormGroup label="Assunto *" name="subject">
        <USelect
          v-model="state.subject"
          :options="subjectOptions"
          option-attribute="name"
          placeholder="-"
        />
      </UFormGroup>

      <UFormGroup label="Nome completo *" name="fullName">
        <UInput v-model="state.fullName" />
      </UFormGroup>

      <UFormGroup label="CPF *" name="cpf">
        <UInput
          v-model="state.cpf"
          v-mask="'###.###.###-##'"
          placeholder="000.000.000-00"
        />
      </UFormGroup>

      <UFormGroup label="Telefone *" name="phone">
        <UInput
          v-model="state.phone"
          v-mask="'(##) #####-####'"
          placeholder="(00) 00000-0000"
        />
      </UFormGroup>

      <UFormGroup label="N칰mero do pedido (se existir)" name="orderNumber">
        <UInput v-model="state.orderNumber" />
      </UFormGroup>

      <UFormGroup
        label="Anexos (opcional)"
        name="requestAttachments"
        class="custom-label"
      >
        <div class="flex items-center justify-center w-full">
          <label
            for="dropzone-file"
            class="flex flex-col items-center justify-center w-full border-2 border-primary border rounded-lg cursor-pointer hover:bg-bray-800 bg-gray-700 hover:border-purple-500 hover:bg-gray-600"
          >
            <div class="flex flex-col items-center justify-center py-3">
              <p class="text-md text-white">
                <span v-if="!fileInput" class="font-semibold"
                  >Clique para anexar um arquivo
                </span>
                <span v-else class="font-semibold">{{
                  state.requestAttachments.name
                }}</span>
              </p>
            </div>
            <UInput
              @change="selectedFileChanged"
              accept="image/*"
              type="file"
              placeholder="Adicione um arquivo"
              id="dropzone-file"
              class="hidden"
              v-model="fileInput"
            />
          </label>
        </div>
        <small class="text-sm text-gray-300">Somente imagens Ex: .png, .jpg</small>
      </UFormGroup>

      <UButton
        block
        type="submit"
        size="lg"
        variant="solid"
        color="primary"
        :loading="isLoading"
        :ui="{ base: 'max-w-[200px] mx-auto' }"
        class="mt-4"
      >
        {{ isLoading ? '' : 'Enviar' }}
      </UButton>
    </UForm>
  </div>
</template>
